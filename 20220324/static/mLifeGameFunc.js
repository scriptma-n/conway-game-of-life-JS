function mRotL(array_)
{
    var n_ = array_.length / 2;

    var R_ = Array();
    for(i=0;i<n_;i++){
        R_[i*2+0] = array_[i*2+1];
        R_[i*2+1] = -array_[i*2];
    }//

    return R_;
}

function mRot180(array_)
{
    var n_ = array_.length / 2;

    var R_ = Array();
    for(i=0;i<n_;i++){
        R_[i*2] = -array_[i*2];
        R_[i*2+1] = -array_[i*2+1];
    }//

    return R_;
}

function mRotR(array_)
{
    var n_ = array_.length / 2;

    var R_ = Array();
    for(i=0;i<n_;i++){
        R_[i*2] = -array_[i*2+1];
        R_[i*2+1] = array_[i*2];
    }//

    return R_;
}

